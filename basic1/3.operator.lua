-- Arithmetic Operators
print("+: " , 1.1 + 1)
print("-: " , 1.1 - 1)
print("*: " , 3 * 3.3)
print("/: " , 3.3 / 3)

-- Relational Operators
print(">: " , 3.3 > 3)
print("<: " , 3.3 < 3)
print(">=: " , 3.3 >= 3)
print("<=: " , 3.3 <= 3)
print(" ~=: " , 3.3 ~= 3)
print(" ==: " , 3.3 == 3)

-- Logical Operators
print(" and: " , 0 and 1)
print(" or: " , 0 or 1)
print(" not: " , not 1)

-- Misc Operators
print("Hello"..",world")
print(#"Hello,world")